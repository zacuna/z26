/** Flattened list of all Tailwind colors for dropdown: { label, value (HSL), family } */
export interface ColorOption {
  label: string;
  value: HSLValue;
  family: string;
}

export const TAILWIND_COLOR_OPTIONS: ColorOption[] = (() => {
  const out: ColorOption[] = [];
  const families = Object.keys(TAILWIND_COLOR_PALETTE).sort();
  for (const family of families) {
    const stops = TAILWIND_COLOR_PALETTE[family];
    for (const stop of Object.keys(stops).sort((a, b) => Number(a) - Number(b))) {
      out.push({
        label: `${family}-${stop}`,
        value: stops[stop],
        family,
      });
    }
  }
  return out;
})();

